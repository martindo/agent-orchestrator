def generate_connector_stub(
    provider_id: str,
    display_name: str,
    capability: str = "EXTERNAL_API",
) -> str:
    """Generate a connector provider Python source file.

    Args:
        provider_id: Unique provider identifier (slug).
        display_name: Human-readable provider name.
        capability: Capability type string.

    Returns:
        Python source code string.
    """
    return _connector_stub(provider_id, display_name, capability)


def generate_event_handler_stub(
    handler_name: str,
    event_types: list[str] | None = None,
) -> str:
    """Generate an event handler Python source file.

    Args:
        handler_name: Handler identifier (slug).
        event_types: Event types to handle.

    Returns:
        Python source code string.
    """
    if event_types is None:
        event_types = ["work_item.submitted", "phase.completed", "workflow.completed"]
    return _event_handler_stub(handler_name, event_types)


def generate_hook_stub(phase_id: str, hook_name: str | None = None) -> str:
    """Generate a phase context hook Python source file.

    Args:
        phase_id: Phase this hook is for.
        hook_name: Override hook function name (defaults to 'hook_{phase_id}').

    Returns:
        Python source code string.
    """
    name = hook_name or f"hook_{phase_id}"
    return _hook_stub(phase_id, name)


def generate_all_stubs(
    team: TeamSpec,
    output_dir: Path,
    *,
    force: bool = False,
) -> ExtensionStubResult:
    """Generate all extension stubs for a team profile.

    Generates:
    - Phase context hooks for phases that have agents (in extensions/hooks/).
    - A default event handler (in extensions/handlers/).

    Does NOT generate connector stubs automatically — those should be
    created on demand via the API when the user wants to integrate a
    specific service.

    Args:
        team: Team specification.
        output_dir: Profile directory to write stubs into.
        force: Override manifest ownership checks.

    Returns:
        ExtensionStubResult with generated files and write status.
    """
    result = ExtensionStubResult()
    extensions_dir = output_dir / "extensions"
    hooks_dir = extensions_dir / "hooks"
    handlers_dir = extensions_dir / "handlers"

    tracker = ManifestTracker(output_dir)

    # Phase hooks
    for phase in team.workflow.phases:
        if phase.is_terminal or not phase.agents:
            continue

        hook_name = f"hook_{phase.id}"
        relative = f"extensions/hooks/{hook_name}.py"
        code = _hook_stub(phase.id, hook_name)
        result.files[relative] = code

        file_path = output_dir / relative
        if not force:
            conflicts = tracker.check_conflicts()
            for conflict in conflicts:
                if relative in conflict:
                    result.skipped.append(relative)
                    continue

        hooks_dir.mkdir(parents=True, exist_ok=True)
        try:
            file_path.write_text(code, encoding="utf-8")
            result.written.append(file_path)
            tracker.mark_user_owned(relative)
        except OSError as exc:
            raise ExtensionStubError(
                f"Failed to write hook stub {file_path}: {exc}"
            ) from exc

    # Default event handler
    handler_code = _event_handler_stub(
        "workflow-events",
        ["work_item.submitted", "phase.completed", "workflow.completed"],
    )
    handler_relative = "extensions/handlers/workflow_events.py"
    result.files[handler_relative] = handler_code

    handler_path = output_dir / handler_relative
    handlers_dir.mkdir(parents=True, exist_ok=True)
    try:
        handler_path.write_text(handler_code, encoding="utf-8")
        result.written.append(handler_path)
        tracker.mark_user_owned(handler_relative)
    except OSError as exc:
        raise ExtensionStubError(
            f"Failed to write event handler stub: {exc}"
        ) from exc

    # Write __init__.py files for the packages
    for init_dir in [extensions_dir, hooks_dir, handlers_dir]:
        init_file = init_dir / "__init__.py"
        if not init_file.exists():
            init_file.write_text(
                f'"""Auto-generated extension package."""\n',
                encoding="utf-8",
            )

    logger.info(
        "Generated %d extension stubs (%d written, %d skipped)",
        len(result.files),
        len(result.written),
        len(result.skipped),
    )
    return result
