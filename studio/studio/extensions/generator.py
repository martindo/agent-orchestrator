"""Extension stub code generator.

Generates ready-to-run Python source files for:
1. **Connector providers** — implement the ConnectorProviderProtocol to
   integrate an external service (e.g. Slack, Jira, custom API).
2. **Event handlers** — subscribe to EventBus events for custom
   side-effects (notifications, metrics, logging).
3. **Phase context hooks** — inject context data into a phase before
   agent execution (e.g. load previous results, external data).

All stubs are fully functional skeletons with real imports, logging,
type hints, and docstrings.  After generation, the user edits the
``TODO`` sections to add their domain logic.
"""

from __future__ import annotations

import logging
from dataclasses import dataclass, field
from pathlib import Path

from studio.exceptions import ExtensionStubError
from studio.ir.models import TeamSpec
from studio.manifest.tracker import ManifestTracker

logger = logging.getLogger(__name__)


@dataclass
class ExtensionStubResult:
    """Result of generating extension stubs.

    Attributes:
        files: Mapping of relative path → generated source code.
        written: Absolute paths of files actually written to disk.
        skipped: Relative paths skipped due to user-ownership.
    """

    files: dict[str, str] = field(default_factory=dict)
    written: list[Path] = field(default_factory=list)
    skipped: list[str] = field(default_factory=list)


